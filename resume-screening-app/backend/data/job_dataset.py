def get_job_by_title(job_title):
    """Get job details by title (case-insensitive)"""
    job_title_lower = job_title.lower()
    for key, job in JOB_DATASET.items():
        if job_title_lower in key or job_title_lower in job['title'].lower():
            return job
    return None

def get_all_job_titles():
    """Get all available job titles"""
    return [job['title'] for job in JOB_DATASET.values()]

def get_skills_for_job(job_title):
    """Get required skills for a specific job"""
    job = get_job_by_title(job_title)
    return job['required_skills'] if job else []

def search_skills(query):
    """Search for skills matching the query"""
    query = query.lower()
    return [skill for skill in SKILLS_DATABASE if query in skill.lower()]

